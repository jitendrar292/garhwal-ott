// Flatten all sentences
const allSentences = Object.values(sentences).flat();

// Generate metadata.csv content
function generateMetadata() {
  return allSentences
    .map((text, i) => {
      const id = `garhwali_${String(i + 1).padStart(3, '0')}`;
      return `${id}|${text}`;
    })
    .join('\n');
}

// Generate JSON format for web recording app
function generateJSON() {
  return allSentences.map((text, i) => ({
    id: `garhwali_${String(i + 1).padStart(3, '0')}`,
    text,
    category: Object.keys(sentences).find((key) =>
      sentences[key].includes(text)
    ),
  }));
}

// Export for use in server
module.exports = {
  sentences,
  allSentences,
  generateMetadata,
  generateJSON,
  totalCount: allSentences.length,
};

// CLI usage
if (require.main === module) {
  const fs = require('fs');
  const args = process.argv.slice(2);

  if (args.includes('--metadata')) {
    fs.writeFileSync('metadata.csv', generateMetadata());
    console.log(`✓ Created metadata.csv with ${allSentences.length} sentences`);
  } else if (args.includes('--json')) {
    fs.writeFileSync('sentences.json', JSON.stringify(generateJSON(), null, 2));
    console.log(`✓ Created sentences.json with ${allSentences.length} sentences`);
  } else {
    console.log(`
Garhwali TTS Training Sentences
===============================
Total: ${allSentences.length} sentences

Categories:
${Object.entries(sentences)
  .map(([key, arr]) => `  - ${key}: ${arr.length} sentences`)
  .join('\n')}

Usage:
  node tts-sentences.js --metadata   Generate metadata.csv for Coqui TTS
  node tts-sentences.js --json       Generate sentences.json for web app
    `);
  }
}
